/* 
    Estrutura de repetição com variável de controle: inicialização, teste lógico e incremento. Sendo o teste verdadeiro, o bloco será executado e o incremento em seguida também

    for (início; teste; incremento) {
        bloco
    }

*/

var c = 1
for (var c = 1; c<=6; c++) {
    console.log(`Passo ${c}`)
}